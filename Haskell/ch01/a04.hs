getInts :: IO [Int]
getInts = unfoldr (BS.readInt . BS.dropWhile isSpace) <$> BS.getLine

solve :: Int -> String
solve n =
  let tmp = loop n [] in
  replicate (10 - length tmp) '0' ++ tmp
  where
    loop n acc
      | n == 0 = acc
      | even n = loop (n `div` 2) ('0' : acc)
      | otherwise = loop (n `div` 2) ('1' : acc)



main :: IO ()
main = do
  n <- readLn @Int
  putStrLn $ solve n